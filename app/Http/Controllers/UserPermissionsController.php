     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $request->validate([
            'name' => 'required',                  
            'code' => 'required',                   
            'priority' => 'required',                   
         ]);

        DB::table('permissions')
            ->updateOrInsert(
                ['permission_code' => $request->code], // Unique identifier column and value
                [
                    'permission_code' => $request->code,
                    'permission_name' => $request->name,
                    'priority' => $request->priority,
                    'updated_at' => now(),
                ]);

       return redirect()->back()->with('success', 'Permission was created successfully');
    }

